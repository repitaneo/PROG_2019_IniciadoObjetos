package objetos;

public class TelefonoMovil {

	private int capacidad;
	private float pulgadas;
	private String marca;
	private String modelo;
	private float peso;
	
	
	public void setCapacidad(int miCapacidad) {
		capacidad = miCapacidad;
	}
	
	public void setPulgadas(float misPulgadas) {
		pulgadas = misPulgadas;
	}
	
	public void setMarca(String miMarca) {
		marca = miMarca;
	}
	
	public void setModelo(String miModelo) {
		modelo = miModelo;
	}
	
	public void setPeso(float miPeso) {
		peso = miPeso;
	}
	
	
	
}
